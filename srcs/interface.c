#include "wolf3d.h"

void	interface(t_wolf *w)
{
	int		y;
	int		x;

	y = 0;
	while (y < WIN_Y)
	{
		x = 0;
		while (x < 400)
		{
			pixel_put_img(x, y, w, 0xFFA600);
			x++;
		}
		y++;
	}
}
